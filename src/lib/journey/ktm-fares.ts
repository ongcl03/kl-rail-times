function fareKey(a: string, b: string): string {
  const numA = parseInt(a, 10);
  const numB = parseInt(b, 10);
  return numA <= numB ? `${a}-${b}` : `${b}-${a}`;
}

export function isKtmKomuterStop(stopId: string): boolean {
  return KTM_KOMUTER_STOPS.has(stopId);
}

export function isKtmNorthernStop(stopId: string): boolean {
  return KTM_NORTHERN_STOPS.has(stopId);
}

export function isKtmEtsStop(stopId: string): boolean {
  return KTM_ETS_STOPS.has(stopId);
}

export function lookupKtmFare(
  fromStopId: string,
  toStopId: string
): JourneyFare | null {
  if (fromStopId === toStopId) return null;

  const key = fareKey(fromStopId, toStopId);
  const cashFare = FARES[key];
  if (cashFare == null) return null;

  const cashless = Math.round(cashFare * 85) / 100;
  const concession = Math.round(cashFare * 50) / 100;

  return {
    cash: cashFare.toFixed(2),
    cashless: cashless.toFixed(2),
    concession: concession.toFixed(2),
  };
}
